# list_creation.py
# Creating lists in Python

# Empty list
empty_list = []
print("Empty list:", empty_list)

# List of integers
numbers = [1, 2, 3, 4, 5]
print("Numbers:", numbers)

# List of strings
fruits = ["apple", "banana", "mango"]
print("Fruits:", fruits)

# Mixed data types
mixed = [1, "apple", 3.5, True]
print("Mixed list:", mixed)

# Nested list
nested = [[1, 2], ["a", "b"]]
print("Nested list:", nested)
