# list_operations.py
# Basic operations on lists

list1 = [1, 2, 3]
list2 = [4, 5, 6]

# Concatenation
list3 = list1 + list2
print("Concatenated list:", list3)

# Repetition
print("Repeated list:", list1 * 2)

# Membership
print("Is 2 in list1?", 2 in list1)
print("Is 5 not in list1?", 5 not in list1)
