# Practice exercises

# 1. Find max and min
numbers = (10, 20, 5, 30, 15)
print("Maximum:", max(numbers))
print("Minimum:", min(numbers))

# 2. Length of tuple
print("Length of tuple:", len(numbers))

# 3. Check membership
print("Is 20 in tuple?", 20 in numbers)

# 4. Reverse tuple
reversed_numbers = numbers[::-1]
print("Reversed tuple:", reversed_numbers)
