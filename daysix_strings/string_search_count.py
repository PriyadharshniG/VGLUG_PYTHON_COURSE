# Searching and counting

text = "Python is powerful. Python is easy."

# Count occurrences
print("Count of 'Python':", text.count("Python"))

# Find first occurrence
print("Index of first 'Python':", text.find("Python"))

# Check start or end
print("Starts with 'Python'? :", text.startswith("Python"))
print("Ends with 'easy'? :", text.endswith("easy"))
