# bitwise_operators_advanced.py
# Advanced Bitwise Operator Examples

a = 12  # 1100
b = 5   # 0101

print("a & b (AND):", a & b)   # 0100 => 4
print("a | b (OR):", a | b)    # 1101 => 13
print("a ^ b (XOR):", a ^ b)   # 1001 => 9
print("~a (NOT):", ~a)         # -13
print("a << 2 (Left Shift):", a << 2)  # 48
print("b >> 1 (Right Shift):", b >> 1) # 2
