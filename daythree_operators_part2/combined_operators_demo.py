
# Using multiple operators together

a = 10
b = 5
c = 20

# Using arithmetic + comparison
if (a + b) > c:
    print("Sum of a and b is greater than c")
else:
    print("Sum of a and b is not greater than c")

# Using logical + membership
fruits = ["apple", "banana", "mango"]
if "apple" in fruits and "grape" not in fruits:
    print("Apple is present, grape is absent")
